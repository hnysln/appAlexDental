package org.dental.utilitarios;

public class ValidaRucEP {

    public static final int NUM_PROVINCIAS = 24;

    public static Boolean validaRucEP(String ruc) {
        final int prov = Integer.parseInt(ruc.substring(0, 2));
        boolean resp = false;

        if (!((prov > 0) && (prov <= NUM_PROVINCIAS))) {
            resp = false;
        }

        // boolean val = false;
        Integer v1, v2, v3, v4, v5, v6, v7, v8, v9;
        Integer sumatoria;
        Integer modulo;
        Integer digito;
        int[] d = new int[ruc.length()];

        for (int i = 0; i < d.length; i++) {
            d[i] = Integer.parseInt(ruc.charAt(i) + "");
        }

        v1 = d[0] * 3;
        v2 = d[1] * 2;
        v3 = d[2] * 7;
        v4 = d[3] * 6;
        v5 = d[4] * 5;
        v6 = d[5] * 4;
        v7 = d[6] * 3;
        v8 = d[7] * 2;
        v9 = d[8];

        sumatoria = v1 + v2 + v3 + v4 + v5 + v6 + v7 + v8;
        modulo = sumatoria % 11;
        if (modulo == 0) {
            return true;
        }
        digito = 11 - modulo;

        resp = digito.equals(v9);
        return resp;
    }
}
